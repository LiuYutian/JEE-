package cn.lyt.test7;

public class Person {
	// 属性
	private String name;
	private String id;
	
	// 无参构造
	public Person() {}

	// 有参构造
	public Person(String name, String id) {
		super();
		this.name = name;
		this.id = id;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public String getId() {
		return id;
	}

	public void setId(String id) {
		this.id = id;
	}
}
